/**
 * Get an icon wrapped in a span with proper styling
 * @param {string} iconName - Name of the icon from AdminIcons
 * @param {string} className - Additional CSS class
 * @returns {string} HTML string with icon
 */
export function getIcon(iconName, className = '') {
    const icon = AdminIcons[iconName];
    if (!icon) {
        console.warn(`[AdminIcons] Icon "${iconName}" not found`);
        return '';
    }
    return `<span class="admin-icon ${className}">${icon}</span>`;
}
